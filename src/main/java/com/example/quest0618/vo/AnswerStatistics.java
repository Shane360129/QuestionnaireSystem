package com.example.quest0618.vo;

public class AnswerStatistics {


    private int subTitleId;
    private String answerOptional;
    private Double statistics;
    private Integer totle;


    public int getSubTitleId() {
        return subTitleId;
    }

    public void setSubTitleId(int subTitleId) {
        this.subTitleId = subTitleId;
    }

    public String getAnswerOptional() {
        return answerOptional;
    }

    public void setAnswerOptional(String answerOptional) {
        this.answerOptional = answerOptional;
    }

    public Double getStatistics() {
        return statistics;
    }

    public void setStatistics(Double statistics) {
        this.statistics = statistics;
    }

    public Integer getTotle() {
        return totle;
    }

    public void setTotle(Integer totle) {
        this.totle = totle;
    }
}
